def get_temperature_category(temperature):
    """Categorize temperature into hot/warm/cool/cold"""
    if temperature >= 25:
        return "hot"
    elif 15 <= temperature < 25:
        return "warm"
    elif 10 <= temperature < 15:
        return "cool"
    else:
        return "cold"


def get_better_alternatives(outfit, temperature):
    """
    Suggest better outfit alternatives based on temperature.
    
    Returns a list of recommended alternatives for the given outfit type and temperature.
    """
    outfit_lower = outfit.lower()
    temp_category = get_temperature_category(temperature)
    
    # Try to find alternatives for the outfit
    if outfit_lower in ALTERNATIVES:
        alternatives = ALTERNATIVES[outfit_lower].get(temp_category, [])
        return alternatives if alternatives else ALTERNATIVES["default"][temp_category]
    
    # Return default if outfit not found
    return ALTERNATIVES["default"][temp_category]
